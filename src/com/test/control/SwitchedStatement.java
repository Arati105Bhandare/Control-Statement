package com.test.control;

public class SwitchedStatement {
	
	public static void main(String [] args)  {
		
		int number =4;
		switch (number) {
		case 1:
			System.out.println("Print number is ....1");
		break;
			
		case 2:
			System.out.println("Print number is....2");
		break;
		
		case 3:
			System.out.println("Print number is ...3");
		break;
		
		
		default:
			System.out.println("Invalid output");
		}
	}

}
